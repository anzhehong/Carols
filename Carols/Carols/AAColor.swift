import Foundation
import UIKit

extension UIColor {
    
    class func GlobalRed() -> UIColor {
        return UIColor(red: 0.7373, green: 0.0824, blue: 0.1882, alpha: 1.0)
    }
    
    class func GlobalGray() -> UIColor {
        return UIColor(red: 0.6824, green: 0.702, blue: 0.7373, alpha: 1.0)
    }
}